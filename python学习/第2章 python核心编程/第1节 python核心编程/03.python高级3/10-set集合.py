'''
集合特点：没有重复值，没有顺序
'''

a = {'a','b','c','d','e','f','g'}
b ={'b','c','d','f','h','x'}

#交集
print(a&b)  #执行结果：{'d', 'f', 'c', 'b'}


#并集
print(a|b)  #执行结果：{'f', 'c', 'b', 'x', 'd', 'a', 'h', 'g', 'e'}


#差值
print(a-b)  #执行结果：{'a', 'g', 'e'}   相当于a减去a和b交集


#对称差值
print(a^b)  #执行结果：{'a', 'h', 'x', 'g', 'e'} 除了a和b交集之外的单独所有元素的集合