'''
集合特点：存储后没有重复的值，没有顺序
'''

a = {11,22,11,44,33,44,55}
print(a)  #执行结果：{33, 11, 44, 22, 55}


#去重方法一：
a = [1,2,4,6,1,2,5,7,9,3,6,9,5,4,3,1]
b = []
for i in a:
    if i not in b:
        b.append(i)
print(b)  #执行结果：[1, 2, 4, 6, 5, 7, 9, 3]


#去重方法二：把列表转换成集合，再由集合转换成列表
a = [1,2,4,6,1,2,5,7,9,3,6,9,5,4,3,1]
b = set(a)
a = list(b)
print(a)  #执行结果：[1, 2, 3, 4, 5, 6, 7, 9]

